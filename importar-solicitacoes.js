const derivarEstado = (sol) => {
  if (sol.dataEntrega)           return 'Entregue';
  if (sol.dataChegada)           return 'Aguardando Entrega';
  if (sol.dataColeta)            return 'Em Trânsito';
  if (sol.dataSolicitacaoColeta) return 'Coleta Solicitada';
  if (sol.dataEmissaoNF)         return 'Aguardando Coleta';
  if (sol.dataSolicitacaoNF)     return 'NF Solicitada';
  if (sol.dataDefinicao)         return 'Aguardando NF';
  return 'Aberto';
};

async function main() {
  let ok = 0, erros = 0;

  for (const sol of solicitacoes) {
    const estado = derivarEstado(sol);
    const status = estado === 'Entregue' ? 'ENCERRADO' : 'EM_ANDAMENTO';

    try {
      await prisma.solicitacaoAtivo.create({
        data: {
          numeroChamado: sol.numeroChamado,
          dataCriacao: sol.dataCriacao,
          descricao: sol.descricao,
          observacoes: sol.observacoes || null,
          tipo: sol.tipo,
          status,
          estado,
          tecnicoId: sol.tecnicoId,
          unidadeId: sol.unidadeId,
          empresaId: EMPRESA_ID,
          projetoId: PROJETO_ID,
          dataDefinicao: sol.dataDefinicao,
          dataDefinicaoConfirmada: sol.dataDefinicaoConfirmada,
          dataSolicitacaoNF: sol.dataSolicitacaoNF,
          dataEmissaoNF: sol.dataEmissaoNF,
          dataSolicitacaoColeta: sol.dataSolicitacaoColeta,
          dataColeta: sol.dataColeta,
          previsaoChegada: sol.previsaoChegada,
          dataChegada: sol.dataChegada,
          dataEntrega: sol.dataEntrega,
          importado: true,
        },
      });
      console.log(`✓ ${sol.numeroChamado}`);
      ok++;
    } catch (e) {
      console.error(`✗ ${sol.numeroChamado}: ${e.message}`);
      erros++;
    }
  }

  console.log(`\nImportadas: ${ok} | Erros: ${erros}`);
}

main().finally(() => prisma.$disconnect());
